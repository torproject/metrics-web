    as.numeric(max(as.Date(c$date, "%Y-%m-%d")) -
    min(as.Date(c$date, "%Y-%m-%d"))))
  ggplot(c, aes(x = date, y = X50., colour = variable)) +
    geom_line(size = 0.75) +
    geom_ribbon(aes(x = date, ymin = X25., ymax = X75., fill = variable),
