        "https://metrics.torproject.org/", sep = ""),
        format = date_breaks$format, major = date_breaks$major,
        minor = date_breaks$minor) +
    scale_y_continuous(name = "", formatter = "percent") +
    scale_colour_hue(name = "Medians and interquartile ranges",
